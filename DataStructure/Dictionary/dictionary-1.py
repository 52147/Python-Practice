# Dictionary

# unordered, mutable, indexed, 
# key-value pair(key must be uniqueness and immutable, such as strings, numbers, tuples)
# value can be any types

# empty dictionary
my_dict = {}

# Dictionary with initial value
my_dict = {
    'name': 'Alice',
    'age': 25,
    'city':'New York'
}

print(my_dict)

# accessing values by key
print(my_dict['name'])

